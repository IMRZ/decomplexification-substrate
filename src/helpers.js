
export function toInt(value) {
  return value | 0;
}

export function randomNum(min, max) {
  if (max) {
    return Math.random() * (max - min) + min;
  }
  return Math.random() * min;
}

export function randomInt(min, max) {
  return toInt(randomNum(min, max));
}


